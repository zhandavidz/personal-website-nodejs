		<h3 class="journal-title">An argument about AI and warfare.</h2>
		<h3 class="date">2/24/18</h3>
		
		<p>
			Many people I know have recently told me about some really opposing views about how AI should be used in warfare. My friend Ben said that AI should be used because it’ll save so many lives, but my other friend Harry said that AI shouldn’t be used in war because it’s not ready yet. I decided to do some research to find out myself.
		</p>
		<p>
			From what I’ve found out, AI really shouldn’t be used in war, as Harry said. First of all, as my fellow BAIR researcher Michael Luo said, the best of AI right now can only learn to recognize cat pictures and play simple games like Atari Breakout <a class="citation" href="/school/isearch/mgp/works-cited/#luo">(Luo)</a>. Bring something like that to the front lines, and who knows what it’s going to do.
		</p>
		<p>
			Also, if you do bring AI to the battlefield and it does something bad, you’re not going to be able to explain what happened and stop that from happening because of the way AI learns. In commercial AI that doesn’t learn, decisions are made through an algorithm, which is like a math equation, and it doesn’t change until you change it. With an algorithm, you can trace what it did to why it did it. However, with learning AI, the most powerful of all AI, it approximates an algorithm, meaning that there aren’t concrete steps to how it got to a conclusion <a class="citation" href="/school/isearch/mgp/works-cited/#luo">(Luo)</a>. Approximating algorithms makes AI more flexible and powerful, but if it does something wrong or bad, you will never know why it did that, and there isn’t a way to prevent that from happening again, a problem on the battlefield.
		</p>
	</article>

