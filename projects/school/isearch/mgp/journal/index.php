		<h3 class="journal-title">Cyberwarfare.</h2>
		<h3 class="date">3/24/18</h3>
		
		<p>
			I was out having a coffee with my fellow researcher Michael that I’ve talked about in previous blogs when he reminded me that we could use AI in cyberwarfare. It blew my mind, because, after thinking so much about using AI in war, I completely forgot about cyberwarfare, something that is so close to AI.
		</p>
		<p>
			Michael told me that something AI would be very good at would be detecting a hacking presence, and learning ways to defend systems from attack, while at the same time learn to hack enemy computers with an efficiency that probably already outpaces humans <a class="citation" href="/school/isearch/mgp/works-cited/#luo">(Luo)</a>. A good way to control the AI is to force it to wait for approval before hacking and to only be able to hack approved targets. AI could also suggest which targets would be easier to hack or have more of an impact. In this way, AI should definitely be used in war, as long as it’s cyberwar.
		</p>
